package com.bean;

public class LotteryLogBean {
	
	private Integer id;
	private String qiNum;
	private Integer Num;
	private String creantime;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getQiNum() {
		return qiNum;
	}
	public void setQiNum(String qiNum) {
		this.qiNum = qiNum;
	}
	public Integer getNum() {
		return Num;
	}
	public void setNum(Integer num) {
		Num = num;
	}
	public String getCreantime() {
		return creantime;
	}
	public void setCreantime(String creantime) {
		this.creantime = creantime;
	}
	
}
